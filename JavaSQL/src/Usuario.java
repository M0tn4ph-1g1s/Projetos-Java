
public class Usuario {
	int id;
	String Nome;
	String Login;
	String Senha;
	
	public void setnome(String nome) {
		this.Nome = Nome;
	}
	
	public void setlogin(String nome) {
		this.Login = Login;
	}
	
	public void setSenha(String nome) {
		this.Senha = Senha;
	}
	
	public String getnome() {
		return Nome;
	}
	
	public String getlogin() {
		return Login;
	}
	
	public String getSenha() {
		return Senha;
	}
}


